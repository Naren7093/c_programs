#include <stdio.h>
int main() {
    unsigned long a = 3000000000UL, b = 1000000000UL;
    printf("Unsigned long addition: %lu\n", a + b);
    printf("Unsigned long subtraction: %lu\n", a - b);
    printf("Unsigned long multiplication: %lu\n", a * b);
    printf("Unsigned long division: %lu\n", a / b);
    return 0;
}

