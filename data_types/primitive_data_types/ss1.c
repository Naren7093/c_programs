#include <stdio.h>
int main() {
    signed short a = 30000, b = 2000;  // Within short range
    printf("Signed short addition: %d\n", a + b);
    printf("Signed short subtraction: %d\n", a - b);
    printf("Signed short multiplication: %d\n", a * b);
    printf("Signed short division: %d\n", a / b);
    return 0;
}

