#include <stdio.h>
int main() {
    signed char a = 100, b = 50;
    printf("Signed char addition: %d + %d = %d\n", a, b, a + b);
    printf("Signed char subtraction: %d - %d = %d\n", a, b, a - b);
    printf("Signed char multiplication: %d * %d = %d\n", a, b, a * b);
    printf("Signed char division: %d / %d = %d\n", a, b, a / b);
    return 0;
}

