#include <stdio.h>
int main() {
    long a = 1000000000L, b = 500000000L;
    printf("Signed long addition: %ld\n", a + b);
    printf("Signed long subtraction: %ld\n", a - b);
    printf("Signed long multiplication: %ld\n", a * b);
    printf("Signed long division: %ld\n", a / b);
    return 0;
}

