#include <stdio.h>
int main() {
    unsigned int x = 4000000000U, y = 2000000000U;
    printf("Unsigned int addition: %u\n", x + y);
    printf("Unsigned int subtraction: %u\n", x - y);
    printf("Unsigned int multiplication: %u\n", x * y);
    printf("Unsigned int division: %u\n", x / y);
    return 0;
}

