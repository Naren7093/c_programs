#include <stdio.h>
int main() {
    unsigned short a = 60000, b = 5000;
    printf("Unsigned short addition: %u + %u = %u\n", a, b, a + b);
    printf("Unsigned short subtraction: %u - %u = %u\n", a, b, a - b);
    printf("Unsigned short multiplication: %u * %u = %u\n", a, b, a * b);
    printf("Unsigned short division: %u / %u = %u\n", a, b, a / b);
    return 0;
}

