#include <stdio.h>
int main() {
    unsigned char a = 250, b = 50;
    printf("Unsigned char addition: %u + %u = %u\n", a, b, a + b);
    printf("Unsigned char subtraction: %u - %u = %u\n", a, b, a - b);
    printf("Unsigned char multiplication: %u * %u = %u\n", a, b, a * b);
    printf("Unsigned char division: %u / %u = %u\n", a, b, a / b);
    return 0;
}

